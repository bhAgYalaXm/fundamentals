package operators;

public class RelationalOperators {

	public static void main(String[] args) {
		
		// Relational Operators
		int a = 5, b = 3;
		System.out.println((a > b) ? "true" : "false");
		System.out.println((a >= b) ? "true" : "false");
		System.out.println((a < b) ? "true" : "false");
		System.out.println((a <= b) ? "true" : "false");
		System.out.println((a == b) ? "true" : "false");
		System.out.println((a != b) ? "true" : "false");
	}

}
